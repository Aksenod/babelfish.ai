/**
 * Генератор уникальных ID для сообщений
 * Использует timestamp + счетчик для гарантии уникальности даже при одновременном создании
 */

// Счетчик для генерации уникальных ID (используется как модульная переменная)
let messageIdCounter = 0;

/**
 * Генерирует уникальный ID для сообщения
 * Использует timestamp + счетчик для гарантии уникальности даже при одновременном создании
 * @returns {number} Уникальный ID сообщения
 */
export const generateMessageId = () => {
  messageIdCounter += 1;
  // Используем timestamp + счетчик для уникальности
  return Date.now() * 1000 + messageIdCounter;
};
